<?php

/**
 * Beautifull Code
 * @param $str
 * @return string
 */
function beautiful($str){

    return "<p>".$str.'</p>';
}

/**
 * @param $str
 * @return string
 */
function bold($str){

    return "<b>".$str.'</b>';
}

/**
 * @param $str
 * @return string
 */
function italic($str){

    return "<i>".$str."</i>";
}





?>